package model;

/**
 *
 * @author Admin
 */
public class Search {
    private int id;
    private String value;
    private String date;

    public Search() {
    }

    public Search(int id, String value, String date) {
        this.id = id;
        this.value = value;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }


    
}
