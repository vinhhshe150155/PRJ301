package model;

/**
 *
 * @author Admin
 */
public class Seat {
    private int id;
    private String row;
    private int number;

    public Seat() {
    }

    public Seat(int id, String row, int number) {
        this.id = id;
        this.row = row;
        this.number = number;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRow() {
        return row;
    }

    public void setRow(String row) {
        this.row = row;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    
}
